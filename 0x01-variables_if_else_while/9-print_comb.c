#include <stdio.h>
#include <stdlib.h>
/**
 * main - prints all possible combinations of single-digit numbers.
 *
 * Return: 0 on successfull completion
 *
 */
int main(void)
{
int num;

for (num = 48; num <= 57; num++)
{
if (num = 57)
{
break;
}
putchar(',');
putchar(' ');
}
putchar('\n');
return (0);
}
